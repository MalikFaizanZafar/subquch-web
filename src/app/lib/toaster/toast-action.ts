/**
 * Enumeration for toast action
 * CloseButtonClick: Action performed on Close icon click
 * Click: Action performed on component click
 * AutoClose: Action performed on auto close of toast after timeout
 */
export enum IsToastAction {
  CloseButtonClick = <any> 'CloseButtonClick',
  Click = <any> 'Click',
  AutoClose = <any> 'AutoClose'
}
